n = int(input())  # сдвиг
text = input()    # закодированное сообщение

decoded_text = ""  # расшифрованное сообщение

for char in text:
    if 'a' <= char <= 'z':  # проверяем, что это строчная латинская буква
        # Получаем код буквы, сдвигаем назад и делаем циклический сдвиг
        new_code = ord(char) - n
        
        # Проверяем, не вышли ли мы за пределы 'a'
        if new_code < ord('a'):
            # Возвращаемся в конец алфавита
            new_code += 26
        
        # Преобразуем код обратно в символ
        decoded_text += chr(new_code)
    else:
        # Если не буква, оставляем как есть (на случай пробелов или других символов)
        decoded_text += char

print(decoded_text)