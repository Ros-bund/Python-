# Считываем число
# В условии не указан текст приглашения ("Введите число:"), 
# поэтому используем пустой input(), чтобы не сломать тест лишним текстом.
try:
    n = int(input("Введите целое число: "))
    print()
except ValueError:
    n = 0

# 1. Заголовок
print(f"Информация о числе {n} :")

# 2. Тип (Python при конвертации в строку сам выдаст <class 'int'>)
print(f"Тип: {type(n)}")

# 3. Четность (Результат сравнения — это True или False)
print(f"Четное: {n % 2 == 0}")

# 4. Количество цифр
# Используем abs(), чтобы убрать знак минус, если число отрицательное
count = len(str(abs(n)))
print(f"Количество цифр: {count}")

# 5. Знак числа
if n > 0:
    print("Число положительное")
elif n < 0:
    print("Число отрицательное")
else:
    print("Это ноль")
