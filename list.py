# Считываем строку с эмодзи
items_string = input()

# Превращаем строку в список
items_list = list(items_string)

# Используем срез [::2], чтобы оставить каждый второй элемент,
# начиная с самого первого (индекс 0, 2, 4...)
result = items_list[::2]

# Выводим итоговый список
print(result)