# Считываем натуральное число n
n = int(input())

# Создаем пустой список для делителей
divisors = []

# Перебираем все числа от 1 до n включительно
for i in range(1, n + 1):
    # Если n делится на i без остатка
    if n % i == 0:
        divisors.append(i)

# Выводим полученный список
print(divisors)